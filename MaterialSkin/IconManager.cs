    public class IconSetting
    {
        public Image this[IconType iconType]
        {
            get
            {
                Image img = null;
                try
                {
                    ResourceManager rm = Resources.ResourceManager;
                    img = (Image)rm.GetObject(iconType.ToString());
                }
                catch (Exception ex)
                {

                }
                return img;
            }
        }
    }

